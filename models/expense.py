class Expense:
    def __init__(self, date, merchant, category, amount, description, id = None):
        self.date = date
        self.merchant = merchant
        self.category = category
        self.amount = amount
        self.description = description
        self.id = id

def get_expense_date(self):
    return self.date

def get_expense_merchant(self):
    return self.merchant

def get_expense_category(self):
    return self.category

def get_expense_amount(self):
    return self.amount

def get_expense_description(self):
    return self.description

def get_expense_id(self):
    return self.id